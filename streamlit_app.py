import pandas as pd
import streamlit as st
import joblib
from pathlib import Path

# =========================
# Paths
# =========================
BASE_DIR = Path(__file__).resolve().parent
MODEL_PATH = BASE_DIR / "models" / "water_quality_model.pkl"
FEATURES_PATH = BASE_DIR / "models" / "feature_columns.pkl"

# =========================
# Page config
# =========================
st.set_page_config(
    page_title="Water Potability Prediction",
    page_icon="💧",
    layout="wide"
)

# =========================
# Cached model loading
# =========================
@st.cache_resource
def load_model_assets():
    model = joblib.load(MODEL_PATH)
    feature_columns = joblib.load(FEATURES_PATH)
    return model, feature_columns

model, feature_columns = load_model_assets()

# =========================
# Header
# =========================
st.title("💧 Water Potability Prediction App")
st.markdown(
    """
    This application predicts whether a water sample is **potable** or **non-potable**
    based on its physicochemical properties.
    """
)

st.info(
    "This prediction is generated by a machine learning model and should not replace certified laboratory analysis."
)

# =========================
# Sidebar
# =========================
st.sidebar.header("Project Information")
st.sidebar.write("**Model:** Random Forest Classifier")
st.sidebar.write("**Target:** Potability")
st.sidebar.write("**Classes:** 0 = Non-Potable, 1 = Potable")

# =========================
# Input form
# =========================
st.subheader("Enter Water Quality Parameters")

with st.form("prediction_form"):
    col1, col2, col3 = st.columns(3)

    with col1:
        ph = st.number_input("pH", min_value=0.0, max_value=14.0, value=8.0, step=0.1)
        hardness = st.number_input("Hardness", min_value=0.0, value=150.0, step=1.0)
        solids = st.number_input("Solids", min_value=0.0, value=22000.0, step=100.0)

    with col2:
        chloramines = st.number_input("Chloramines", min_value=0.0, value=10.0, step=0.1)
        sulfate = st.number_input("Sulfate", min_value=0.0, value=250.0, step=1.0)
        conductivity = st.number_input("Conductivity", min_value=0.0, value=425.0, step=1.0)

    with col3:
        organic_carbon = st.number_input("Organic Carbon", min_value=0.0, value=14.0, step=0.1)
        trihalomethanes = st.number_input("Trihalomethanes", min_value=0.0, value=66.0, step=1.0)
        turbidity = st.number_input("Turbidity", min_value=0.0, value=4.0, step=0.1)

    submitted = st.form_submit_button("Predict Potability")

# =========================
# Prediction block
# =========================
if submitted:
    input_data = pd.DataFrame([{
        "ph": ph,
        "Hardness": hardness,
        "Solids": solids,
        "Chloramines": chloramines,
        "Sulfate": sulfate,
        "Conductivity": conductivity,
        "Organic_carbon": organic_carbon,
        "Trihalomethanes": trihalomethanes,
        "Turbidity": turbidity
    }])

    input_data = input_data[feature_columns]

    prediction = model.predict(input_data)[0]
    probabilities = model.predict_proba(input_data)[0]

    potable_prob = probabilities[1] * 100
    non_potable_prob = probabilities[0] * 100

    # =========================
    # Result section
    # =========================
    st.subheader("Prediction Result")

    result_col1, result_col2 = st.columns([2, 1])

    with result_col1:
        if prediction == 1:
            st.success("✅ Predicted class: POTABLE")
        else:
            st.error("❌ Predicted class: NON-POTABLE")

        st.write(f"**Probability of Potable Water:** {potable_prob:.2f}%")
        st.write(f"**Probability of Non-Potable Water:** {non_potable_prob:.2f}%")

        confidence = max(potable_prob, non_potable_prob)
        if confidence >= 80:
            st.info("Model confidence is high.")
        elif confidence >= 60:
            st.info("Model confidence is moderate.")
        else:
            st.warning("Model confidence is relatively low. Interpret with caution.")

    with result_col2:
        st.metric("pH", f"{ph:.2f}")
        st.metric("Sulfate", f"{sulfate:.2f}")
        st.metric("Hardness", f"{hardness:.2f}")
        
        # =========================
    # Domain warnings
    # =========================
    st.subheader("Domain Warnings")

    warnings_list = []

    # pH recommended drinking-water range
    if ph < 6.5 or ph > 8.5:
        warnings_list.append("pH is outside the commonly recommended drinking-water range (6.5 - 8.5).")

    # Turbidity
    if turbidity > 5:
        warnings_list.append("Turbidity is elevated. High turbidity may indicate poor water clarity or suspended particles.")

    # Trihalomethanes
    if trihalomethanes > 100:
        warnings_list.append("Trihalomethanes level is high. Elevated values may be undesirable from a water quality perspective.")

    # Chloramines
    if chloramines > 10:
        warnings_list.append("Chloramines level is unusually high.")

    # Sulfate
    if sulfate > 400:
        warnings_list.append("Sulfate level is high and may affect taste or water acceptability.")

    # Conductivity
    if conductivity < 50 or conductivity > 800:
        warnings_list.append("Conductivity is outside the expected operational range and should be interpreted carefully.")

    # Organic carbon
    if organic_carbon > 20:
        warnings_list.append("Organic carbon is relatively high, which may indicate elevated organic matter.")

    # Solids
    if solids > 40000:
        warnings_list.append("Total dissolved solids are very high.")

    if warnings_list:
        for warning in warnings_list:
            st.warning(f"⚠️ {warning}")
    else:
        st.success("No major domain warning detected for the entered values.")
        
        
    st.info('The model follows patterns learned from the dataset and does not enforce regulatory drinking-water thresholds. As a result, some predictions may appear counterintuitive from a domain perspective.')

    # =========================
    # Probability chart
    # =========================
    st.subheader("Prediction Probabilities")

    prob_df = pd.DataFrame({
        "Class": ["Non-Potable", "Potable"],
        "Probability": [non_potable_prob, potable_prob]
    })

    st.bar_chart(prob_df.set_index("Class"))

    # =========================
    # Input summary
    # =========================
    st.subheader("Input Summary")
    st.dataframe(input_data, use_container_width=True)